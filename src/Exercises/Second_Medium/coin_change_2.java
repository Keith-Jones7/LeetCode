package Exercises.Second_Medium;

import java.util.Arrays;
import java.util.*;

//518 零钱兑换2
public class coin_change_2 {
    public int change(int amount, int[] coins) {
        int[] dp = new int[amount + 1];
        dp[0] = 1;
        for (int coin : coins) {
            for (int i = coin; i <= amount; i++) {
                dp[i] += dp[i - coin];
            }
        }
        return dp[amount];
    }
}
